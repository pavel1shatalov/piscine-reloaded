#include <stdio.h>

int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int n;

	i = 0;
	n = 0;
	while (tab[i])
	{
		if (*tab[i] == '0')
			i++;
		if (f(tab[i++]) == 1)
			n++;
	}
	return (n);
}
