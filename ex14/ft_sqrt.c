int		ft_sqrt(int nb)
{
	int c;

	if (nb <= 0)
		return (0);
	if (nb >= 100)
	{
		c = nb / 10;
		while (c > 0)
		{
			if (nb == c * c)
				return (c);
			c--;
		}
	}
	else
	{
		c = nb;
		while (c > 0)
		{
			if (nb == c * c)
				return (c);
			c--;
		}
	}
	return (0);
}
